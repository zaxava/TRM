package dao.dtt.DDTProject;

public class DTTProcessingCard {
	private int requestId;
	private String lastName;
	private String firstName;
	private String trainingType;
	private String trainingModule;
	private String trainingModuleScope;
	private String startDate;
	private String endDate;
	private String location;
	private int approxParticipants;
	
	public int getRequestId() {
		return requestId;
	}
	public void setRequestId(int requestId) {
		this.requestId = requestId;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getTrainingType() {
		return trainingType;
	}
	public void setTrainingType(String trainingType) {
		this.trainingType = trainingType;
	}
	public String getTrainingModule() {
		return trainingModule;
	}
	public void setTrainingModule(String trainingModule) {
		this.trainingModule = trainingModule;
	}
	public String getTrainingModuleScope() {
		return trainingModuleScope;
	}
	public void setTrainingModuleScope(String trainingModuleScope) {
		this.trainingModuleScope = trainingModuleScope;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getApproxParticipants() {
		return approxParticipants;
	}
	public void setApproxParticipants(int approxParticipants) {
		this.approxParticipants = approxParticipants;
	}
	
	
}
